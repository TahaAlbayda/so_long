#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	size_t	len;
	int		i;

	len = ft_strlen(s);
	d = (char *)malloc((len + 1) * sizeof(char));
	if (!d)
		return (NULL);
	if (!s)
		return (NULL);
	i = 0;
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
